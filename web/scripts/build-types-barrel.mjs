import { readdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const generatedDir = resolve(here, "../src/gql/__generated__");
const barrelPath = resolve(generatedDir, "all-types.generated.ts");

const skip = new Set(["all-types.generated.ts", "graphql.schemas.generated.ts"]);

const modules = readdirSync(generatedDir)
  .filter(f => f.endsWith(".generated.ts") && !skip.has(f))
  .map(f => f.replace(/\.ts$/, ""))
  .sort();

const header = `// Auto-generated by scripts/build-types-barrel.mjs.
// Re-exports every \`*.generated.ts\` so \`graphql.schemas.generated.ts\` can
// resolve operation result types scattered by the near-operation-file preset.
// Do not edit by hand — run \`yarn gql\` to regenerate.
`;

const body = modules.map(m => `export * from "./${m}";`).join("\n") + "\n";

writeFileSync(barrelPath, header + "\n" + body);
